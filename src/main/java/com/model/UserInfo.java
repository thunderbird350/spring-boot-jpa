package com.model;

/**
 * @author MD AZAR
 *
 */
public class UserInfo {

	private String name;
	private String surname;
	private String role;
	private String userPhoneCompany;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the surname
	 */
	public String getSurname() {
		return surname;
	}

	/**
	 * @param surname
	 *            the surname to set
	 */
	public void setSurname(String surname) {
		this.surname = surname;
	}

	/**
	 * @return the role
	 */
	public String getRole() {
		return role;
	}

	/**
	 * @param role
	 *            the role to set
	 */
	public void setRole(String role) {
		this.role = role;
	}

	/**
	 * @return the userPhoneCompany
	 */
	public String getUserPhoneCompany() {
		return userPhoneCompany;
	}

	/**
	 * @param userPhoneCompany
	 *            the userPhoneCompany to set
	 */
	public void setUserPhoneCompany(String userPhoneCompany) {
		this.userPhoneCompany = userPhoneCompany;
	}
}
